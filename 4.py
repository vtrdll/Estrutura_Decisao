#Faça um Programa que verifique se uma letra digitada é vogal ou consoante.


letra = input("Digite uma letra: ")

if letra == 'a' or letra ==  'A' or letra == 'e' or letra == 'E' or letra == 'i' or letra == 'I' or letra == 'o' or letra == 'O' or letra == 'U':
        print("VOGAL!")
else:
    print("COSOANTE!")



